<?php 
function jsize_string($str)
{
	$str=str_replace(    
    array('!', '"', '#', '$', '%', '&', '\'', '(', ')', '*',    
        '+', ', ', '-', '.', '/', ':', ';', '<', '=', '>',    
        '?', '@', '[', '\\', ']', '^', '_', '`', '{', '|',    
        '}', '~', '；', '﹔', '︰', '﹕', '：', '，', '﹐', '、',    
        '．', '﹒', '˙', '·', '。', '？', '！', '～', '‥', '‧',    
        '′', '〃', '〝', '〞', '‵', '‘', '’', '『', '』', '「',    
        '」', '“', '”', '…', '❞', '❝', '﹁', '﹂', '﹃', '﹄', PHP_EOL,"\n"),    
    '',    
    $str);
	return $str;
}
